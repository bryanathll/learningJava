package pert5;

public class operatorTernary {
    public static void main(String[] args) {
        boolean suka = true;
        String jawaban;

        //memggunakan operator ternary
        jawaban = suka ? "Ya" : "Tidak";

        //menampilkan jawaban
        System.out.println("kamu suka saya: "+jawaban);
        
    }
    
}
